/**
 * Сложная
 *
 * Перевести натуральное число n > 0 в римскую систему.
 * Римские цифры: 1 = I, 4 = IV, 5 = V, 9 = IX, 10 = X, 40 = XL, 50 = L,
 * 90 = XC, 100 = C, 400 = CD, 500 = D, 900 = CM, 1000 = M.
 * Например: 23 = XXIII, 44 = XLIV, 100 = C
 */
fun roman(n: Int): String {
    var str = ""
    var num = n
    var last = n % 10
    when (last) {
        1 -> str = "I$str"
        2 -> str = "II$str"
        3 -> str = "III$str"
        4 -> str = "IV$str"
        5 -> str = "V$str"
        6 -> str = "VI$str"
        7 -> str = "VII$str"
        8 -> str = "VIII$str"
        9 -> str = "IX$str"
        else -> str += ""
    }
    num /= 10
    last = num % 10
    when (last) {
        1 -> str = "X$str"
        2 -> str = "XX$str"
        3 -> str = "XXX$str"
        4 -> str = "XL$str"
        5 -> str = "L$str"
        6 -> str = "LX$str"
        7 -> str = "LXX$str"
        8 -> str = "LXXX$str"
        9 -> str = "XC$str"
        else -> str += ""
    }
    num /= 10
    last = num % 10
    when (last) {
        1 -> str = "C$str"
        2 -> str = "CC$str"
        3 -> str = "CCC$str"
        4 -> str = "CD$str"
        5 -> str = "D$str"
        6 -> str = "DC$str"
        7 -> str = "DCC$str"
        8 -> str = "DCCC$str"
        9 -> str = "CM$str"
        else -> str += ""
    }
    num /= 10
    last = num % 10
    when (last) {
        1 -> str = "M$str"
        2 -> str = "MM$str"
        3 -> str = "MMM$str"
        else -> str += ""
    }
    return str
}

